package Lesson15;
import java.util.Scanner;
import java.util.Random;

public class PinningTasks {
    public static void main(String[] args) {
        Scanner s1 = new Scanner(System.in);
        int num = s1.nextInt();

        while (num >= 1){

            System.out.println(num);
            num--;
        }
    }
}
